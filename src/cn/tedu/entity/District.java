package cn.tedu.entity;



public class District{

	private Integer id;		
	private String name;	
	
	public District() {
		super();
	}
	public District(Integer id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}